package com.realdolmen.bookstore;

public class Book {

	private String name;

	public Book(String name) {
		super();
		this.name = name;
	}

	public Book() {
		super();
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
